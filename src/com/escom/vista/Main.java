    private void listarArticulos() {
        try {
            ArticuloCliente help = new ArticuloCliente();
            List<Articulo> lista;
            lista = help.getAllArticulos();
            DefaultTableModel modelo = null;
            try {
                modelo = (DefaultTableModel) jTable1.getModel();
                int a = modelo.getRowCount();
                for (int i = 0; i < a; i++) {
                    modelo.removeRow(0);
                }
            } catch (Exception e) {
                System.out.println(e);
            }

            String datos[] = new String[4];

            for (int i = 0; i < lista.size(); i++) {
                datos[0] = Integer.toString(lista.get(i).getIdarticulo());
                datos[1] = lista.get(i).getNombre();
                datos[2] = Integer.toString(lista.get(i).getExistencia());
                datos[3] = Double.toString(lista.get(i).getPrecio());
                modelo.addRow(datos);
            }

        } catch (RemoteException | NotBoundException | MalformedURLException ex) {
            System.out.println(ex.toString());
        }

    }
    
     private void listarMovimientos() {
        try {
            MovimientosCliente help = new MovimientosCliente();
            List<MovArticulo> lista;
            lista = help.getAllMovimientos();
            DefaultTableModel modelo = null;
            try {
                modelo = (DefaultTableModel) jTable2.getModel();
                int a = modelo.getRowCount();
                for (int i = 0; i < a; i++) {
                    modelo.removeRow(0);
                }
            } catch (Exception e) {
                System.out.println(e);
            }

            String datos[] = new String[5];

            for (int i = 0; i < lista.size(); i++) {
                datos[0] = Integer.toString(lista.get(i).getIdmovimiento());
                datos[1] = lista.get(i).getFecha().toString();
                datos[2] = lista.get(i).getTipo()+" ";
                datos[3] = lista.get(i).getCantidad();
                datos[4] = lista.get(i).getArticulo().getIdarticulo()+" ";
                modelo.addRow(datos);
            }

        } catch (RemoteException | NotBoundException | MalformedURLException ex) {
            System.out.println(ex.toString());
        }

    }   

