    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            Date fecha = new Date();
            MovimientosCliente movCliente = new MovimientosCliente();
            ArticuloCliente arti = new ArticuloCliente();
            char tipo = 0;
            Articulo art = arti.getArticuloById(id);
            MovArticulo mov = new MovArticulo();
            if (jComboBox1.getSelectedIndex() == 0) {
                tipo = 'E';
                mov.setTipo(tipo);
                mov.setCantidad(String.valueOf(jSpinner2.getValue()));
                mov.setArticulo(art);
                mov.setFecha(new java.sql.Date(fecha.getTime()));
                art.setExistencia(art.getExistencia() + Integer.parseInt(String.valueOf(jSpinner2.getValue())));
                arti.uptadeArticulo(art);
                movCliente.addMovimiento(mov);
//                 System.out.println(mov.getFecha() + " " + mov.getCantidad());
//                System.out.println(art.getIdarticulo() + " " + art.getExistencia());
                listarArticulos();
                listarMovimientos();
            } else if (jComboBox1.getSelectedIndex() == 1 && art.getExistencia() >= Integer.parseInt(String.valueOf(jSpinner2.getValue()))) {
                tipo = 'S';
                mov.setTipo(tipo);
                mov.setCantidad(String.valueOf(jSpinner2.getValue()));
                mov.setArticulo(art);
                mov.setFecha(new java.sql.Date(fecha.getTime()));
                art.setExistencia(art.getExistencia() - Integer.parseInt(String.valueOf(jSpinner2.getValue())));
                arti.uptadeArticulo(art);
                movCliente.addMovimiento(mov);
//                 System.out.println(mov.getFecha() + " " + mov.getCantidad());
//                System.out.println(art.getIdarticulo() + " " + art.getExistencia());
                listarArticulos();
                listarMovimientos();
            }else{
                JOptionPane.showMessageDialog(this, "Articulos insuficientes");
            }
        } catch (RemoteException | NotBoundException | MalformedURLException ex) {
            ex.printStackTrace();
        }

    }//GEN-LAST:event_jButton4ActionPerformed

